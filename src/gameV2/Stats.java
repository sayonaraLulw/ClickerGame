package gameV2;

public class Stats {
	// Variables
	private int score = 0;
	private int power = 0;
	private int autoclick = 0;
	
	// Getter and setter
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	public int getPower() {
		return power;
	}
	public void setPower(int power) {
		this.power = power;
	}
	public int getAutoclick() {
		return autoclick;
	}
	public void setAutoclick(int autoclick) {
		this.autoclick = autoclick;
	}
}